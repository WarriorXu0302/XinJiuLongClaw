async def main() -> None:
    inserted = 0
    updated = 0
    async with admin_session_factory() as s:
        for code, parent, name, level in HENAN:
            existing = (await s.execute(
                select(MallRegion).where(MallRegion.area_code == code)
            )).scalar_one_or_none()
            if existing:
                changed = (
                    existing.parent_code != parent
                    or existing.name != name
                    or existing.level != level
                )
                if changed:
                    existing.parent_code = parent
                    existing.name = name
                    existing.level = level
                    updated += 1
                continue
            s.add(MallRegion(
                area_code=code, parent_code=parent, name=name, level=level,
            ))
            inserted += 1
        await s.commit()
    print(f"河南省行政区划 seed 完成：新增 {inserted}，更新 {updated}，总数 {len(HENAN)}")


if __name__ == "__main__":
    asyncio.run(main())
